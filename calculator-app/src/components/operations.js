const addOpt = (currentNum, newNum) => {
    return currentNum + newNum;
}

const subtractOpt = (currentNum, newNum) => {
    return currentNum - newNum;
}

const multiplyOpt = (currentNum, newNum) => {
    return currentNum * newNum;
}

const divideOpt = (currentNum, newNum) => {
    return currentNum / newNum;
}

const percentageOpt = (currentNum) => {
    return currentNum / 100;
}